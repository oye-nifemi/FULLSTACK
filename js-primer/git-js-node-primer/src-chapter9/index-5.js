// Grouping instructions to carry out specific tasks

// by declaration
function addTwoNumbers(a, b) {
    return a + b;
}
console.log(addTwoNumbers(5, 6));

// by expression
var addTwoNumbers_1 = function(a,b){
    return a + b;
} 
console.log(addTwoNumbers_1(5,7))

// Passing by Value or by Reference